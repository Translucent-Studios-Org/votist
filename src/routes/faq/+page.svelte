<div class="mx-auto max-w-3xl px-4 py-8 md:px-6 md:py-12">
	<h1 class="mb-6 text-2xl font-bold text-votist-blue md:text-3xl">Frequently Asked Questions</h1>
	<div class="space-y-6 text-gray-700">
		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">
				Is Votist affiliated with the City of San Rafael?
			</h2>
			<p>
				No. The current pilot is independently organized and operated. Votist is not commissioned
				by, funded by, or formally partnered with the City of San Rafael or any public agency.
				Public officials may view the results, but no institution controls the process or its
				conclusions.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">
				Does Votist replace public meetings or elections?
			</h2>
			<p>
				No. Votist does not replace existing democratic processes. It does not issue binding
				decisions, endorsements, or mandates. It functions as a listening instrument designed to
				clarify how participants reason once they engage with shared context.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">
				Is this statistically representative of the public?
			</h2>
			<p>
				No. Votist does not claim statistical representativeness. Participation is voluntary and
				self-selected. The goal is not to model the entire population, but to make informed
				participant sentiment more legible.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">Why require a knowledge check?</h2>
			<p>
				Votist introduces a brief knowledge check to establish a shared baseline of familiarity
				with the governing framework shaping a decision. The purpose is structural, not
				ideological. It confirms awareness of the legal, regulatory, and institutional constraints
				involved so that participation occurs within shared context.
			</p>
			<p>Understanding a framework does not require agreeing with it.</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">
				Who determines the content of the knowledge materials?
			</h2>
			<p>
				Materials are drawn from official documents, public records, and related briefing
				materials relevant to the issue under discussion. The knowledge check is designed to
				confirm familiarity with governing constraints, not to privilege a particular policy
				position.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">
				What if I disagree with the governing framework?
			</h2>
			<p>
				Participants are free to disagree with current laws, policies, or regulatory structures.
				Votist anchors discussion to the framework that currently governs the decision, because
				meaningful disagreement requires shared awareness of existing constraints.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">
				Why require real-name participation?
			</h2>
			<p>
				Real-name participation introduces civic accountability and reduces performative or
				abusive behavior. Identity functions as friction, not as a credential or badge of
				expertise. It encourages participants to engage as members of a shared community rather
				than as anonymous avatars.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">
				Is my identity publicly displayed?
			</h2>
			<p>
				Only limited identity information is displayed publicly. Aggregated results do not
				identify individual participants. Personal information is not made public beyond what is
				disclosed at sign-in.
			</p>
			<p>
				(For detailed information, see the <a
					href="/privacy"
					class="text-[#167B9B] hover:underline">Data and Privacy page</a
				>.)
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">
				Who owns and controls the data?
			</h2>
			<p>
				The Votist project operates and controls the platform. Participation data is aggregated
				for public display. Votist does not sell personal data. Data governance practices are
				documented separately.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">
				What happens to the results?
			</h2>
			<p>
				Results are displayed in aggregated form and may be reviewed by residents, civic leaders,
				journalists, and community members. The results are advisory and informational. They do
				not determine policy outcomes.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">Is Votist partisan?</h2>
			<p>
				No. Votist does not endorse political parties or advocate specific policy outcomes. Its
				role is to structure participation around shared governing frameworks so that informed
				values and tradeoffs become more visible.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">
				Is Votist owned or operated by a corporation?
			</h2>
			<p>
				No. Votist is independently organized as a private civic initiative and is not owned or
				operated by a corporation, political party, advocacy organization, developer, or real
				estate entity.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">How will success be measured?</h2>
			<p>
				The pilot tests whether participants are willing to engage with modest friction and shared
				context before weighing in. Indicators of success include completion rates, clarity of
				aggregated responses, and whether the resulting output is viewed as more useful than
				conventional comment-based participation.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">
				Can Votist expand to other issues?
			</h2>
			<p>
				If the pilot demonstrates viability, the model may be applied to additional public issues.
				Expansion would remain grounded in issue-specific governing frameworks and structured
				participation.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">How can I get involved?</h2>
			<p>
				See the <a href="/support" class="text-[#167B9B] hover:underline">Get Involved page</a> for
				ways to contribute, collaborate, or support the experiment.
			</p>
		</section>
	</div>
</div>

<div class="mx-auto max-w-3xl px-4 py-8 md:px-6 md:py-12">
	<h1 class="mb-6 text-2xl font-bold text-votist-blue md:text-3xl">
		Votist Moderation and Platform Policy
	</h1>
	<div class="space-y-6 text-gray-700">
		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">Moderation Authority</h2>
			<p>
				Moderation authority rests with the Votist project. Moderation authority is exercised by
				the platform operator or designated administrators acting on behalf of the Votist project.
				The platform retains discretion to review, approve, remove, restrict, or limit user
				contributions in order to maintain structured, respectful participation consistent with
				the platform's stated design principles.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">Removal Criteria</h2>
			<p>Content may be removed or restricted if it:</p>
			<ul class="ml-6 list-disc space-y-1">
				<li>Contains harassment, threats, or abusive language</li>
				<li>Includes personal attacks or defamatory statements</li>
				<li>Promotes unlawful conduct</li>
				<li>Attempts to evade or undermine the knowledge-gating framework</li>
				<li>
					Repeatedly ignores platform structure after notice or attempts to derail discussion
					outside the established framework
				</li>
				<li>Shares private personal information without consent</li>
			</ul>
			<p>
				Moderation decisions are based on platform conduct standards, not political viewpoint.
				Disagreement with governing frameworks or policy outcomes is permitted. Violations of
				conduct standards are not.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">Escalation Procedures</h2>
			<p>
				If a participant repeatedly violates platform standards, the Votist project may restrict
				posting privileges or suspend access to the platform. Escalation decisions are made
				internally by the platform operator or designated administrators.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">Appeal Process</h2>
			<p>
				Participants may request review of a moderation action by submitting a written inquiry
				through the platform's designated <a
					href="/support"
					class="text-[#167B9B] hover:underline">contact channel</a
				>. Moderation decisions are reviewed internally and are final within the platform's
				governance structure.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">Retention and Logging Policy</h2>
			<p>
				User participation data may be logged for purposes of platform integrity, moderation
				review, system improvement, and aggregated reporting. Third-party service providers may
				process limited data necessary to operate the platform. Aggregated results are displayed
				publicly without identifying individual participants. Votist does not sell personal data.
				Data handling practices may evolve as the platform develops.
			</p>
		</section>

		<section class="space-y-3">
			<h2 class="text-lg font-semibold text-votist-blue">AI Assistance</h2>
			<p>
				Automated systems may assist in content review and platform integrity processes. Final
				moderation authority remains with the Votist project.
			</p>
		</section>
	</div>
</div>
